export function getForumCategory(key) {
  const normalized = String(key || 'GERAL').toUpperCase();
  return FORUM_CATEGORIES[normalized] || FORUM_CATEGORIES.GERAL;
}

export function formatForumDate(dateStr) {
  if (!dateStr) return '';
  const date = new Date(dateStr);
  return date.toLocaleDateString('pt-BR', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    hour: '2-digit',
    minute: '2-digit',
  });
}

